package Aula_3_If_While_For_Case_Operadores.Exemplos;

public class Exemplo_2 {
    public static void main(String[] args) {
        
        for (int i = 0; i <= 10; i++) { // Para a variável "i" iniciada em 1, enquanto "i" for menor ou igual a 10, incrementa o valor de "i" em 1:
            System.out.println(i); // Imprime o valor de "i" na tela.
        }
    }
}